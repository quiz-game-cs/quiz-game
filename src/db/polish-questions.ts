async function main() {
  // 1) inseop user 확보
  let user = await db.select().from(users).where(eq(users.nickname, "inseop")).limit(1);
  if (user.length === 0) {
    [user[0]] = await db.insert(users).values({ nickname: "inseop" }).returning();
    console.log(`created user 'inseop': ${user[0].id}`);
  } else {
    console.log(`found user 'inseop': ${user[0].id}`);
  }
  const authorId = user[0].id;

  // 2) 기존 53개 questions를 created_at ASC 순으로 가져옴
  const qs = await db.select().from(questions).orderBy(asc(questions.createdAt));
  if (qs.length !== 53) {
    console.error(`expected 53 existing questions, got ${qs.length}. Abort.`);
    process.exit(1);
  }

  // 3) POLISHED idx별 UPDATE
  console.log(`Updating ${POLISHED.length} polished questions...`);
  for (const p of POLISHED) {
    const q = qs[p.idx];
    await db
      .update(questions)
      .set({ text: p.text, answers: p.answers, authorId })
      .where(eq(questions.id, q.id));
    console.log(`  [${p.idx + 1}] updated: ${p.text.slice(0, 30)}...`);
  }

  // 4) NEW_QUESTIONS INSERT
  console.log(`Inserting ${NEW_QUESTIONS.length} new questions...`);
  const cats = await db.select().from(categories);
  const codeToId = new Map<string, string>(
    cats.map((c) => [c.code, c.id])
  );
  for (const nq of NEW_QUESTIONS) {
    const catId = codeToId.get(nq.categoryCode);
    if (!catId) {
      console.error(`missing category code ${nq.categoryCode}. Abort.`);
      process.exit(1);
    }
    await db.insert(questions).values({
      text: nq.text,
      answers: nq.answers,
      categoryId: catId,
      authorId,
    });
    console.log(`  inserted (${nq.categoryCode}): ${nq.text.slice(0, 30)}...`);
  }

  console.log("Done.");
}

main().catch((e) => {
  console.error("polish failed:", e);
  process.exit(1);
});
